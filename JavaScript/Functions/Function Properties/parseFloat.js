// parseFloat(string)

console.log(parseFloat("4"));      // 4
console.log(parseFloat("4.666"));  // 4.666
console.log(parseFloat("4.444"));  // 4.444
console.log(parseFloat(4));        // 4
console.log(parseFloat(4.666));    // 4.666
console.log(parseFloat(NaN));      // NaN
console.log(parseFloat(Infinity)); // Infinity