/* How to create a variable: var variableName: Type = value
    The var keyword specifies the beginning of a variable declaration.
    It means a mutable variable which means its value can change throughout the program.
*/ 


/* Types:

String: a sequence of characters enclosed in double quotes
Integer (Int): whole numbers and their opposites
Boolean: true or false values 
Double: floating-point numbers
Float: Other floating-point numbers

*/

fun main() {
    var sern: String = "Kotlin User";

    println(sern); // Kotlin User

    var currentWeather: String;
    currentWeather = "Sunny"; // Also works

    println(currentWeather);
  
}