/*Math.ceil() accepts a Double value and returns the smallest value that is greater than or equal to the given value.

Math.sqrt() accepts a Double value and returns its square root.

Math.abs() accepts an Int value and returns its absolute value.

Math.floor() accepts a Double value and returns the largest value that is less than or equal to it.
*/

fun main() {
    println(Math.ceil(45.5466));
    println(Math.sqrt(9.9));
    println(Math.abs(-9));
    println(Math.abs(9));
    println(Math.floor(45.5466));
}