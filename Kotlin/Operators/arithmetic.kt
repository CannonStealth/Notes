/* Operators List:
+ addition
- subtraction
* multiplication
/ division
% modulo (divides and gives the remainder)
*/

/* Note: The order of operations can be specified using parentheses.
For example, the use of parentheses in score = 5 * (4 + 3)
sets score equal to 5 * 7 rather than 20 + 3.
*/

fun main() {

var score = 0;
// score is 0

println(score)
 
score = 4 + 2;
// it is now 6

println(score)
 
score = 4 - 2;
// it is now 2

println(score)
 
score = 4 * 2;
// it is now 8

println(score)
 
score = 4 / 2;
// and now 2

println(score)
 
score = 5 % 2;
// and now 1

println(score)

}