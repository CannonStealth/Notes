/*
== equal to
!= not equal to
> greater than
< less than
>= greater than or equal to
<= less than or equal to

5 != 4 true
4 < 8 true
3 > 5 false
8 <= 8 true 
 */

 fun main() {

 if (5 > 4) {
     println("5 > 4")
 }
 if (5 != 4) {
      println("5 isn't equal to 4")
 }
 if (5 >= 4) { 
     println("5 >= 4")
 }
 }